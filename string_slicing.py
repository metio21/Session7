s = "0123456789"
print(s)
print(s[1:2]) # first index is included, last is excluded
print(s[1:7])
print(s[:7]) # if you omit the first index, it starts from the beginning
s = "I go to school early in the morning"
print(s[:20])
print(s[20:]) # includes all the way to end

print(s[:]) # the whole thing
print(s[::2])# this means step 2
print(s[::-1])
print("racecar"[::-1])


